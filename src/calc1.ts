export const calc = (a: number, b: number): number => {
	let a1 = a
	let b1 = b
	if (a < b) {
		a1 = b
		b1 = a
	}
	while (b1 != 0) {
		const remainder = a1 % b1
		a1 = b1
		b1 = remainder
	}
	return a1
}
